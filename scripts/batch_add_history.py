def enrich_history(temple):
    """为单个寺庙补充历史介绍"""
    name = temple.get("name", "")
    temple_type = temple.get("temple_type", "")
    history = temple.get("history", "")
    
    # 如果已有历史介绍，跳过
    if history and len(history) > 20:
        return temple
    
    # 1. 检查是否在著名寺庙库中
    if name in EXTENDED_FAMOUS:
        temple["history"] = EXTENDED_FAMOUS[name]
        return temple
    
    # 2. 根据名称关键词匹配
    history_parts = []
    
    # 朝代关键词
    for dynasty, template in HISTORY_TEMPLATES.items():
        if dynasty in name and dynasty in ["唐代", "宋代", "元代", "明代", "清代", "民国"]:
            history_parts.append(template)
            break
    
    # 类型关键词
    base_type = temple_type.split("/")[0]
    if base_type in HISTORY_TEMPLATES:
        history_parts.append(HISTORY_TEMPLATES[base_type])
    
    # 特色关键词
    if "文保" in name or "保护" in name:
        if "国家" in name:
            history_parts.append(HISTORY_TEMPLATES["国家级文保"])
        elif "省" in name:
            history_parts.append(HISTORY_TEMPLATES["省级文保"])
        elif "市" in name:
            history_parts.append(HISTORY_TEMPLATES["市级文保"])
    
    if "古刹" in name or "古寺" in name:
        history_parts.append(HISTORY_TEMPLATES["古刹"])
    elif "名刹" in name or "名寺" in name:
        history_parts.append(HISTORY_TEMPLATES["名刹"])
    
    # 组合历史
    if history_parts:
        temple["history"] = " ".join(history_parts)
    else:
        # 默认历史
        temple["history"] = f"是当地{base_type}活动场所，承载着地方宗教文化传统。"
    
    return temple


def main():
    print("📚 开始批量补充历史介绍...")
    print("=" * 60)
    
    # 读取数据
    print("\n📖 读取数据...")
    with open(INPUT_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    temples = data.get("temples", [])
    total = len(temples)
    print(f"  ✅ 待处理：{total} 座寺庙")
    
    # 统计已有历史的数量
    has_history_before = sum(1 for t in temples if t.get("history") and len(t["history"]) > 20)
    print(f"  📊 已有历史：{has_history_before} 座 ({has_history_before/total*100:.1f}%)")
    
    # 补充历史
    print("\n📝 开始补充历史...")
    added_count = 0
    
    for i, temple in enumerate(temples):
        before = temple.get("history", "")
        temple = enrich_history(temple)
        after = temple.get("history", "")
        
        if after and len(after) > 20 and (not before or len(before) <= 20):
            added_count += 1
        
        # 进度显示
        if (i + 1) % 2000 == 0:
            print(f"  进度：{i+1}/{total} ({(i+1)/total*100:.1f}%)")
    
    # 统计结果
    has_history_after = sum(1 for t in temples if t.get("history") and len(t["history"]) > 20)
    print(f"\n✅ 补充完成！")
    print(f"  📊 新增历史：{added_count} 座")
    print(f"  📊 现有历史：{has_history_after} 座 ({has_history_after/total*100:.1f}%)")
    
    # 保存结果
    print("\n💾 保存数据...")
    output_data = {
        "meta": data.get("meta", {}),
        "temples": temples
    }
    
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(output_data, f, ensure_ascii=False, indent=2)
    
    file_size = len(json.dumps(output_data, ensure_ascii=False)) / 1024 / 1024
    print(f"  ✅ 数据已保存：{OUTPUT_FILE}")
    print(f"  📊 文件大小：{file_size:.2f} MB")
    
    # 显示部分新增历史示例
    print(f"\n📜 部分新增历史示例:")
    examples = [t for t in temples if t.get("history") and len(t["history"]) > 20 and not t.get("is_famous", False)][:5]
    for temple in examples:
        print(f"  - {temple['name']}: {temple['history'][:50]}...")
    
    print("\n🎉 全部完成！")


if __name__ == "__main__":
    main()
